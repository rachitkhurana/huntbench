#!/usr/bin/env python3
"""jobsdb - a tiny, dependency-free job-hunting database + mission control.

Storage: NDJSON (one JSON object per line) at jobs.ndjson next to this script.
Markdown snapshots (overview.md, pipeline.md, shortlist.md) are regenerated by
`render` and are fully derived from the DB - never edit them by hand. The web
dashboard (`serve`) is the main view.

Usage:
    ./jobsdb.py <command> [options]
    ./jobsdb.py <command> -h      # per-command help

Commands:
    setup      Scaffold config/ (profile, portals, master CV) from the example files.
    doctor     Check your setup: config, Chrome, optional claude CLI, database.
    reset      Clear the database (--demo reseeds the demo dataset).
    add        Add one record (JSON on stdin, --json, or --field flags). Dedupe by id.
    bulk-add   Add many records from a JSON array or NDJSON file (--file).
    list       List records with filters (alias: filter).
    filter     Alias for list.
    update     Update fields of a record by id.
    enrich     Mark a record as needing full JD (--need) or attach enrichment (--attach).
    dedupe     Collapse duplicate ids (keep richest/newest), rewrite the file.
    stats      Print funnel counts (status), region counts, fit distribution.
    render     Regenerate the markdown view notes from the DB (idempotent).
    promote    Set status=applied + promoted_to path; print an opportunity scaffold.
    dashboard  Launch the interactive split-pane TUI (browse, triage, promote).
    scan       Pull fresh openings from ATS portals (portals.yml) into the DB (zero tokens).
    liveness   Check job URLs; flag/prune expired postings.
    profile    Show the resolved shared profile.yml (comp, target roles, archetypes).
    cv         Generate a tailored, JD-keyworded CV + cover letter (PDF via Chrome).
    tailor     AI-tailor a CV to a job's JD via `claude -p`, then render it.
    apply      Build an apply packet (CV + mapped form fields + drafted answers) for a job.
    serve      Launch the local web GUI (browser dashboard) over jobs.ndjson.
    activity   Log a timeline event (interview/email/note) on a job; optionally set status.
"""

import argparse
import datetime
import json
import os
import sys

HERE = os.path.dirname(os.path.abspath(__file__))
DB_PATH = os.path.join(HERE, "jobs.ndjson")
TODAY = datetime.date.today().isoformat()

# ---- controlled vocabularies -------------------------------------------------

REGIONS = ["uae", "remote-emea", "europe", "australia", "india", "other"]
WORK_MODES = ["onsite", "hybrid", "remote", "unknown"]
# funnel order matters for stats + rendering
STATUSES = ["new", "shortlisted", "skip", "applied",
            "screening", "interviewing", "offer", "closed", "passed"]

REGION_LABEL = {
    "uae": "UAE (Dubai / Abu Dhabi)",
    "remote-emea": "Remote (EMEA)",
    "europe": "Europe",
    "australia": "Australia",
    "india": "India",
    "other": "Other",
}

# ---- inference helpers -------------------------------------------------------

UAE_KW = ["dubai", "abu dhabi", "sharjah", "uae", "united arab emirates", "emirates"]
EUROPE_KW = ["london", "uk", "united kingdom", "england", "berlin", "munich", "amsterdam",
             "netherlands", "germany", "paris", "france", "ireland", "dublin", "spain",
             "portugal", "lisbon", "madrid", "europe", "poland", "warsaw", "sweden",
             "stockholm", "zurich", "switzerland", "copenhagen", "denmark", "eu"]
AUS_KW = ["australia", "sydney", "melbourne", "brisbane", "perth", "canberra"]
INDIA_KW = ["india", "bangalore", "bengaluru", "mumbai", "delhi", "gurgaon", "gurugram",
            "hyderabad", "pune", "noida", "chennai", "kolkata"]


def infer_work_mode(loc):
    l = (loc or "").lower()
    if "hybrid" in l:
        return "hybrid"
    if "remote" in l:
        return "remote"
    if "on-site" in l or "onsite" in l or "on site" in l:
        return "onsite"
    return "unknown"


def infer_region(loc, work_mode="unknown"):
    l = (loc or "").lower()
    if any(k in l for k in UAE_KW):
        return "uae"
    if any(k in l for k in EUROPE_KW):
        return "remote-emea" if work_mode == "remote" else "europe"
    if any(k in l for k in AUS_KW):
        return "australia"
    if any(k in l for k in INDIA_KW):
        return "india"
    if "remote" in l:
        return "remote-emea"
    return "other"


# ---- fit scoring -------------------------------------------------------------

STRONG_TITLE = ["frontend", "front end", "front-end", "react", "next.js", "nextjs",
                "design engineer", "creative", "animation", "webgl", "three.js",
                "threejs", "gsap", "ui engineer", "product engineer", "forward deployed",
                "typescript", "web developer", "fullstack", "full stack", "full-stack",
                "creative technologist"]
CREATIVE_SIGNAL = ["creative", "animation", "webgl", "three", "gsap", "3d", "motion",
                   "interactive", "design engineer", "shader", "creative technologist"]
NEGATIVE_TITLE = ["backend", "back end", "back-end", "data engineer", "devops", "sre",
                  "android", "ios ", "qa ", "quality", "test engineer", "embedded",
                  "firmware", "security engineer", "machine learning", "ml engineer",
                  "game", "unity", "salesforce", "php", "wordpress", ".net",
                  "java developer", "python developer", "golang", "rust "]
LEAD_KW = ["lead", "staff", "principal", "head of", "technical manager",
           "engineering manager"]

# lazily-loaded shared profile (career-ops profile.yml); best-effort, optional
_PROFILE_CACHE = None


def _profile():
    global _PROFILE_CACHE
    if _PROFILE_CACHE is None:
        try:
            import configlib
            _PROFILE_CACHE = configlib.load_profile()[0] or {}
        except Exception:
            _PROFILE_CACHE = {}
    return _PROFILE_CACHE


def _profile_title_kws():
    try:
        import configlib
        return configlib.profile_keywords(_profile())
    except Exception:
        return []


def score_fit(rec):
    t = (rec.get("title") or "").lower()
    tags = " ".join(rec.get("tags") or []).lower()
    hay = t + " " + tags
    score = 3.0
    # title signal
    hits = sum(1 for k in STRONG_TITLE if k in t)
    if hits >= 1:
        score += 1.0
    if hits >= 2:
        score += 0.5
    if any(k in t for k in NEGATIVE_TITLE):
        score -= 1.5
    # creative / animation specialty signal (a default bonus; tune to taste)
    if any(k in hay for k in CREATIVE_SIGNAL):
        score += 1.0
    # seniority match (lenient: senior good, entry penalised, director tolerated)
    exp = (rec.get("experience_tag") or "").lower()
    if exp in ("internship", "entry"):
        score -= 1.0
    elif exp == "associate":
        score -= 0.5
    elif exp in ("mid-senior", "mid", "senior"):
        score += 0.5
    # leadership scope bonus (he is a lead)
    if any(k in t for k in LEAD_KW):
        score += 0.5
    # profile-aware boost: title matches a target-role / archetype from profile.yml
    kws = _profile_title_kws()
    if kws and any(k in t for k in kws):
        score += 0.5
    return int(max(1, min(5, round(score))))


def reason_fit(rec):
    t = (rec.get("title") or "").lower()
    bits = []
    if any(k in t for k in CREATIVE_SIGNAL):
        bits.append("animation/creative match")
    if any(k in t for k in STRONG_TITLE):
        bits.append("frontend/product title")
    if any(k in t for k in NEGATIVE_TITLE):
        bits.append("off-stack title (penalised)")
    bits.append(REGION_LABEL.get(rec.get("region_bucket", "other"), "other"))
    if any(k in t for k in LEAD_KW):
        bits.append("leadership scope")
    exp = (rec.get("experience_tag") or "").lower()
    if exp in ("internship", "entry", "associate"):
        bits.append("junior seniority (penalised)")
    return "; ".join(bits)


# ---- record normalization ----------------------------------------------------

def normalize(raw):
    rec = dict(raw)
    rec["id"] = str(rec.get("id", "")).strip()
    rec.setdefault("company", "")
    rec.setdefault("title", "")
    rec.setdefault("location", "")
    wm = rec.get("work_mode") or infer_work_mode(rec.get("location"))
    rec["work_mode"] = wm if wm in WORK_MODES else "unknown"
    rec["region_bucket"] = rec.get("region_bucket") or infer_region(
        rec.get("location"), rec["work_mode"])
    if rec["region_bucket"] not in REGIONS:
        rec["region_bucket"] = "other"
    rec.setdefault("source", "linkedin-search")
    rec.setdefault("search_query", "")
    if not rec.get("url") and rec["id"]:
        rec["url"] = "https://www.linkedin.com/jobs/view/%s/" % rec["id"]
    rec.setdefault("url", "")
    rec.setdefault("date_posted", None)
    rec.setdefault("date_found", TODAY)
    rec.setdefault("experience_tag", "unknown")
    rec.setdefault("salary", None)
    rec.setdefault("status", "new")
    if rec["status"] not in STATUSES:
        rec["status"] = "new"
    rec.setdefault("tags", [])
    rec.setdefault("notes", "")
    rec.setdefault("enriched", False)
    rec.setdefault("enrichment", None)
    rec.setdefault("promoted_to", None)
    rec.setdefault("created", TODAY)
    rec["updated"] = rec.get("updated") or TODAY
    if not rec.get("fit_score"):
        rec["fit_score"] = score_fit(rec)
    if not rec.get("fit_reason"):
        rec["fit_reason"] = reason_fit(rec)
    return rec


ACTIVITY_KINDS = ["applied", "screening", "interview", "offer", "reject", "email", "note"]


def add_activity(rec, entry):
    """Append a timeline entry to rec['activity'] (created on demand). Returns the entry.
    entry keys: date, kind, title, detail, contact, link, thread_id, source. Stamps 'added'."""
    e = {k: v for k, v in (entry or {}).items() if v not in (None, "")}
    e.setdefault("date", TODAY)
    e.setdefault("kind", "note")
    e.setdefault("source", "manual")
    e["added"] = TODAY
    acts = rec.get("activity") or []
    acts.append(e)
    rec["activity"] = acts
    return e


# ---- storage -----------------------------------------------------------------

DEMO_PATH = os.path.join(HERE, "examples", "demo-jobs.ndjson")


def _seed_if_missing():
    """First run: if there's no jobs.ndjson yet, seed it from the demo dataset so the
    dashboard isn't empty. `./jobsdb.py reset` clears it when you're ready for real data."""
    if os.path.exists(DB_PATH) or not os.path.exists(DEMO_PATH):
        return
    try:
        with open(DEMO_PATH, "r", encoding="utf-8") as f:
            raw = [json.loads(ln) for ln in f if ln.strip()]
        save_db([normalize(r) for r in raw])
    except Exception:  # noqa - never let seeding crash a command
        pass


def load_db():
    _seed_if_missing()
    if not os.path.exists(DB_PATH):
        return []
    out = []
    with open(DB_PATH, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                out.append(json.loads(line))
            except json.JSONDecodeError:
                sys.stderr.write("warning: skipping unparseable line\n")
    return out


def save_db(records):
    tmp = DB_PATH + ".tmp"
    with open(tmp, "w", encoding="utf-8") as f:
        for r in records:
            f.write(json.dumps(r, ensure_ascii=False, sort_keys=True) + "\n")
    os.replace(tmp, DB_PATH)


def index_by_id(records):
    return {r["id"]: r for r in records if r.get("id")}


def merge_record(old, new):
    """Merge new into old: new non-empty scalar fields win; keep created; bump updated."""
    merged = dict(old)
    for k, v in new.items():
        if k in ("created",):
            continue
        if v in ("", None, [], {}):
            continue
        merged[k] = v
    # union tags
    tags = list(dict.fromkeys((old.get("tags") or []) + (new.get("tags") or [])))
    if tags:
        merged["tags"] = tags
    merged["updated"] = TODAY
    return merged


def upsert(records, incoming):
    """Insert or merge a list of normalized records. Returns (added, updated)."""
    idx = index_by_id(records)
    added = updated = 0
    for rec in incoming:
        if not rec.get("id"):
            sys.stderr.write("warning: record without id skipped\n")
            continue
        if rec["id"] in idx:
            merged = merge_record(idx[rec["id"]], rec)
            idx[rec["id"]].clear()
            idx[rec["id"]].update(merged)
            updated += 1
        else:
            records.append(rec)
            idx[rec["id"]] = rec
            added += 1
    return added, updated


# ---- input parsing -----------------------------------------------------------

def read_records_from_stdin_or_json(args):
    """Collect one-or-more raw records from --json, stdin, or --field flags."""
    raws = []
    if getattr(args, "json", None):
        parsed = json.loads(args.json)
        raws = parsed if isinstance(parsed, list) else [parsed]
    elif not sys.stdin.isatty():
        data = sys.stdin.read().strip()
        if data:
            data_s = data.lstrip()
            if data_s.startswith("["):
                raws = json.loads(data_s)
            elif "\n" in data_s and not data_s.startswith("{"):
                raws = [json.loads(l) for l in data_s.splitlines() if l.strip()]
            else:
                # could be NDJSON of objects OR a single object
                try:
                    raws = [json.loads(data_s)]
                except json.JSONDecodeError:
                    raws = [json.loads(l) for l in data_s.splitlines() if l.strip()]
    # flag-built record (used alone, or to override a single stdin record)
    flag_rec = {}
    for key in ("id", "company", "title", "location", "work_mode",
                "region_bucket", "search_query", "url", "date_posted",
                "experience_tag", "salary", "status", "notes"):
        val = getattr(args, key, None)
        if val is not None:
            flag_rec[key] = val
    if getattr(args, "fit", None) is not None:
        flag_rec["fit_score"] = args.fit
    if getattr(args, "tags", None):
        flag_rec["tags"] = [t.strip() for t in args.tags.split(",") if t.strip()]
    if not raws and flag_rec:
        raws = [flag_rec]
    elif raws and flag_rec:
        raws = [dict(r, **flag_rec) for r in raws]
    return raws


# ---- filtering ---------------------------------------------------------------

def apply_filters(records, args):
    out = records
    if getattr(args, "region", None):
        out = [r for r in out if r.get("region_bucket") == args.region]
    if getattr(args, "status", None):
        out = [r for r in out if r.get("status") == args.status]
    if getattr(args, "company", None):
        c = args.company.lower()
        out = [r for r in out if c in (r.get("company") or "").lower()]
    if getattr(args, "min_fit", None) is not None:
        out = [r for r in out if (r.get("fit_score") or 0) >= args.min_fit]
    if getattr(args, "keyword", None):
        k = args.keyword.lower()
        out = [r for r in out
               if k in (r.get("title") or "").lower()
               or k in (r.get("company") or "").lower()
               or k in " ".join(r.get("tags") or []).lower()
               or k in (r.get("notes") or "").lower()]
    if getattr(args, "enriched", None) is not None:
        out = [r for r in out if bool(r.get("enriched")) == args.enriched]
    return out


# ---- markdown rendering ------------------------------------------------------

def esc(s):
    """Escape a value for use inside a markdown pipe-table cell."""
    return str(s if s is not None else "").replace("|", r"\|").replace("\n", " ")


def link_cell(rec, from_note="pipeline"):
    url = rec.get("url") or ""
    return "[open](%s)" % url if url else ""


def funnel_counts(records):
    counts = {s: 0 for s in STATUSES}
    for r in records:
        counts[r.get("status", "new")] = counts.get(r.get("status", "new"), 0) + 1
    return counts


def region_counts(records):
    counts = {r: 0 for r in REGIONS}
    for rec in records:
        counts[rec.get("region_bucket", "other")] = counts.get(
            rec.get("region_bucket", "other"), 0) + 1
    return counts


def fit_counts(records):
    counts = {i: 0 for i in range(1, 6)}
    for r in records:
        fs = r.get("fit_score") or 0
        if fs in counts:
            counts[fs] += 1
    return counts


def frontmatter(title, ntype, tags):
    return "> Generated by `./jobsdb.py render` on %s — do not hand-edit.\n" % TODAY


def render_index(records):
    total = len(records)
    fc = funnel_counts(records)
    rc = region_counts(records)
    fits = fit_counts(records)
    shortlisted = sum(1 for r in records if (r.get("fit_score") or 0) >= 4)
    enriched = sum(1 for r in records if r.get("enriched"))
    promoted = sum(1 for r in records if r.get("promoted_to"))

    out = [frontmatter("Overview", "dashboard", [])]
    out.append("\n# Huntbench — overview\n")
    out.append("A local, queryable database of jobs matched to your profile, auto-scored for "
               "fit. The web dashboard (`./jobsdb.py serve`) is the main view; this is a "
               "generated snapshot.\n")
    out.append("\n---\n\n## Funnel\n")
    out.append("| Stage | Count |\n|-------|------:|")
    for s in STATUSES:
        out.append("| %s | %d |" % (s, fc[s]))
    out.append("| **total** | **%d** |" % total)
    out.append("\n## By region\n")
    out.append("| Region | Count |\n|--------|------:|")
    for r in REGIONS:
        out.append("| %s | %d |" % (REGION_LABEL[r], rc[r]))
    out.append("\n## Fit distribution\n")
    out.append("| Fit | Count |\n|-----|------:|")
    for i in range(5, 0, -1):
        out.append("| %d | %d |" % (i, fits[i]))
    out.append("\n## Snapshot\n")
    out.append("- Shortlist (fit ≥ 4): **%d**" % shortlisted)
    out.append("- Enriched (full JD pulled): **%d**" % enriched)
    out.append("- Promoted to opportunities: **%d**\n" % promoted)
    out.append("\n---\n\n## How to use\n")
    out.append("```bash\n"
               "./jobsdb.py serve                       # web dashboard (browser)\n"
               "./jobsdb.py scan                        # pull fresh jobs from ATS boards (config/portals.yml)\n"
               "./jobsdb.py list --region uae --min-fit 4\n"
               "./jobsdb.py update --id <id> --status shortlisted\n"
               "./jobsdb.py cv --id <id>                # render a tailored CV + cover letter\n"
               "./jobsdb.py apply --id <id>             # build an apply packet, then say 'apply to <id>' to your agent\n"
               "./jobsdb.py render                      # regenerate these snapshots\n"
               "```\n")
    out.append("See **AGENTS.md** for the full agent-driven workflow (onboarding, discovery, "
               "tailoring, applying, inbox sync).\n")
    return "\n".join(out) + "\n"


def render_pipeline(records):
    out = [frontmatter("Pipeline — Career Ops", "dashboard",
                       ["career", "job-search", "linkedin", "pipeline", "dashboard"])]
    out.append("\n# Pipeline\n")
    out.append("All tracked jobs, grouped by region then status. Generated by "
               "`jobsdb.py render`.\n")
    out.append("\n---\n")
    active = [r for r in records if r.get("status") != "skip"]
    for region in REGIONS:
        regrecs = [r for r in active if r.get("region_bucket") == region]
        if not regrecs:
            continue
        out.append("\n## %s (%d)\n" % (REGION_LABEL[region], len(regrecs)))
        for status in STATUSES:
            if status == "skip":
                continue
            rows = [r for r in regrecs if r.get("status") == status]
            if not rows:
                continue
            rows.sort(key=lambda r: (-(r.get("fit_score") or 0),
                                     (r.get("company") or "").lower()))
            out.append("\n### %s (%d)\n" % (status, len(rows)))
            out.append("| Fit | Company | Title | Work | Exp | Posted | Link |")
            out.append("|:---:|---------|-------|------|-----|--------|------|")
            for r in rows:
                out.append("| %d | %s | %s | %s | %s | %s | %s |" % (
                    r.get("fit_score") or 0,
                    esc(r.get("company")),
                    esc(r.get("title")),
                    esc(r.get("work_mode")),
                    esc(r.get("experience_tag")),
                    esc(r.get("date_posted") or "—"),
                    link_cell(r, "pipeline"),
                ))
    # skipped section (collapsed count only)
    skipped = sum(1 for r in records if r.get("status") == "skip")
    out.append("\n---\n\n_Skipped / discarded: %d (hidden)._\n" % skipped)
    return "\n".join(out) + "\n"


def render_shortlist(records):
    out = [frontmatter("Shortlist — Career Ops", "dashboard",
                       ["career", "job-search", "linkedin", "shortlist", "dashboard"])]
    out.append("\n# Shortlist\n")
    out.append("Top-fit roles (fit ≥ 4), excluding skipped. Generated by "
               "`jobsdb.py render`.\n")
    out.append("\n---\n\n")
    rows = [r for r in records
            if (r.get("fit_score") or 0) >= 4 and r.get("status") != "skip"]
    rows.sort(key=lambda r: (-(r.get("fit_score") or 0),
                             REGIONS.index(r.get("region_bucket", "other")),
                             (r.get("company") or "").lower()))
    out.append("| Fit | Region | Company | Title | Status | Why | Link |")
    out.append("|:---:|--------|---------|-------|--------|-----|------|")
    for r in rows:
        out.append("| %d | %s | %s | %s | %s | %s | %s |" % (
            r.get("fit_score") or 0,
            esc(r.get("region_bucket")),
            esc(r.get("company")),
            esc(r.get("title")),
            esc(r.get("status")),
            esc(r.get("fit_reason")),
            link_cell(r, "shortlist"),
        ))
    if not rows:
        out.append("\n_No fit ≥ 4 jobs yet._")
    return "\n".join(out) + "\n"


def write_note(name, content):
    path = os.path.join(HERE, name)
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)
    return path


# ---- commands ----------------------------------------------------------------

def cmd_add(args):
    raws = read_records_from_stdin_or_json(args)
    if not raws:
        sys.exit("add: no input (use --json, stdin, or --id/--title/... flags)")
    recs = [normalize(r) for r in raws]
    db = load_db()
    added, updated = upsert(db, recs)
    save_db(db)
    print("add: +%d new, %d merged (total %d)" % (added, updated, len(db)))


def cmd_bulk_add(args):
    with open(args.file, "r", encoding="utf-8") as f:
        data = f.read().strip()
    raws = []
    if data.lstrip().startswith("["):
        raws = json.loads(data)
    else:
        for line in data.splitlines():
            line = line.strip()
            if line:
                raws.append(json.loads(line))
    recs = [normalize(r) for r in raws]
    db = load_db()
    added, updated = upsert(db, recs)
    save_db(db)
    print("bulk-add: read %d, +%d new, %d merged (total %d)"
          % (len(recs), added, updated, len(db)))


def cmd_list(args):
    db = load_db()
    rows = apply_filters(db, args)
    rows.sort(key=lambda r: (-(r.get("fit_score") or 0),
                             REGIONS.index(r.get("region_bucket", "other"))))
    if getattr(args, "limit", None):
        rows = rows[:args.limit]
    if getattr(args, "json", False):
        for r in rows:
            print(json.dumps(r, ensure_ascii=False))
        return
    print("%-3s %-5s %-12s %-26s %-30s %-13s" %
          ("fit", "stat", "region", "company", "title", "id"))
    print("-" * 96)
    for r in rows:
        print("%-3s %-5s %-12s %-26s %-30s %-13s" % (
            r.get("fit_score", ""),
            (r.get("status", "") or "")[:5],
            (r.get("region_bucket", "") or "")[:12],
            (r.get("company", "") or "")[:26],
            (r.get("title", "") or "")[:30],
            r.get("id", ""),
        ))
    print("\n%d record(s)." % len(rows))


def cmd_update(args):
    db = load_db()
    idx = index_by_id(db)
    if args.id not in idx:
        sys.exit("update: id %s not found" % args.id)
    rec = idx[args.id]
    if args.status:
        if args.status not in STATUSES:
            sys.exit("update: bad status; choose from %s" % ", ".join(STATUSES))
        rec["status"] = args.status
    if args.fit is not None:
        rec["fit_score"] = max(1, min(5, args.fit))
    if args.notes is not None:
        rec["notes"] = args.notes
    if args.add_tag:
        tags = rec.get("tags") or []
        for t in args.add_tag.split(","):
            t = t.strip()
            if t and t not in tags:
                tags.append(t)
        rec["tags"] = tags
    if args.promoted_to is not None:
        rec["promoted_to"] = args.promoted_to
    if args.rescore:
        rec["fit_score"] = score_fit(rec)
        rec["fit_reason"] = reason_fit(rec)
    rec["updated"] = TODAY
    save_db(db)
    print("update: %s -> status=%s fit=%s" %
          (args.id, rec.get("status"), rec.get("fit_score")))


def cmd_enrich(args):
    db = load_db()
    idx = index_by_id(db)
    if args.id not in idx:
        sys.exit("enrich: id %s not found" % args.id)
    rec = idx[args.id]
    if args.need:
        rec["enriched"] = False
        tags = rec.get("tags") or []
        if "needs-jd" not in tags:
            tags.append("needs-jd")
        rec["tags"] = tags
        print("enrich: %s flagged needs-jd" % args.id)
    elif args.attach:
        with open(args.attach, "r", encoding="utf-8") as f:
            enr = json.load(f)
        enr.setdefault("fetched_at", TODAY)
        rec["enrichment"] = enr
        rec["enriched"] = True
        rec["tags"] = [t for t in (rec.get("tags") or []) if t != "needs-jd"]
        # skills can strengthen fit score
        if enr.get("skills"):
            rec["tags"] = list(dict.fromkeys(
                rec["tags"] + [s.lower() for s in enr["skills"]]))
        rec["fit_score"] = score_fit(rec)
        rec["fit_reason"] = reason_fit(rec)
        print("enrich: %s enriched (fit now %s)" % (args.id, rec["fit_score"]))
    else:
        sys.exit("enrich: pass --need or --attach <file.json>")
    rec["updated"] = TODAY
    save_db(db)


def cmd_dedupe(args):
    db = load_db()
    best = {}
    for r in db:
        rid = r.get("id")
        if not rid:
            continue
        if rid not in best:
            best[rid] = r
        else:
            keep = best[rid]
            score_r = sum(1 for v in r.values() if v not in ("", None, [], {}))
            score_k = sum(1 for v in keep.values() if v not in ("", None, [], {}))
            if (score_r, r.get("updated", "")) > (score_k, keep.get("updated", "")):
                best[rid] = merge_record(keep, r)
            else:
                best[rid] = merge_record(r, keep)
    result = list(best.values())
    removed = len(db) - len(result)
    save_db(result)
    print("dedupe: removed %d duplicate(s), %d unique remain" % (removed, len(result)))


