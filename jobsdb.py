def cmd_reset(args):
    """Clear the job database (removing the demo/real data). --demo reseeds the demo set."""
    if not args.yes:
        print("reset: this clears jobs.ndjson (%d jobs). Re-run with --yes to confirm."
              % len(load_db()))
        return 1
    if args.demo and os.path.exists(DEMO_PATH):
        with open(DEMO_PATH, encoding="utf-8") as f:
            raw = [json.loads(ln) for ln in f if ln.strip()]
        save_db([normalize(r) for r in raw])
        print("reset: reseeded %d demo jobs." % len(raw))
    else:
        save_db([])
        print("reset: database cleared. Add real jobs via scan / bulk-add / add.")
    return 0


