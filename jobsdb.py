def build_opportunity_scaffold(rec, slug, status=None):
    """Return (relpath, scaffold_text) for graduating a record into an application folder
    under output/<slug>/<slug>.md. Mutates rec: sets promoted_to, bumps status to `status`
    (default 'applied') when still in a discovery state, stamps updated. Does NOT save."""
    path = "output/%s/%s.md" % (slug, slug)
    rec["promoted_to"] = path
    if rec.get("status") in ("new", "shortlisted", "skip"):
        rec["status"] = status or "applied"
    rec["updated"] = TODAY
    fm = (
        "# %s — %s\n\n"
        "- **Status:** %s\n- **Location:** %s (%s)\n- **Salary:** %s\n"
        "- **Source id:** %s\n- **Date:** %s\n- **JD:** %s\n\n"
        "## Notes\n\n%s\n"
    ) % (
        rec.get("company"), rec.get("title"), rec["status"],
        rec.get("location"), rec.get("work_mode"), rec.get("salary") or "TBC",
        rec.get("id"), TODAY, rec.get("url") or "", rec.get("notes") or "",
    )
    return path, fm


